async function run() {
  const pool = await getPool();
  let updated = 0;

  for (const [placeId, urls] of Object.entries(placeImages)) {
    const pid = parseInt(placeId);
    // Delete old images
    await pool.request()
      .input('pid', sql.Int, pid)
      .query('DELETE FROM PlaceImages WHERE place_id=@pid');

    // Insert new images
    for (let i = 0; i < urls.length; i++) {
      await pool.request()
        .input('pid', sql.Int, pid)
        .input('url', sql.NVarChar, urls[i])
        .input('sort', sql.Int, i)
        .query('INSERT INTO PlaceImages (place_id, image_url, sort_order) VALUES (@pid, @url, @sort)');
    }

    updated++;
    if (updated % 10 === 0) console.log(`Updated ${updated} places...`);
  }

  console.log(`✅ Done! Updated images for ${updated} places.`);
  process.exit(0);
}

run().catch(e => { console.error(e); process.exit(1); });
